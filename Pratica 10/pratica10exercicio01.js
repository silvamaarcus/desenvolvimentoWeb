function visibilidadeDetalhes() { 
    if (document.getElementById("divDetalhes").style.display == "") {     
    document.getElementById("divDetalhes").style.display = "none";     
    }
    else {
    document.getElementById("divDetalhes").style.display = "";     
    }     
    return false;     
}

